    private void importarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_importarbtnActionPerformed
        try {
            JFileChooser escojer = new JFileChooser();
            int eleccion = escojer.showOpenDialog(this);
            
            if (eleccion == JFileChooser.APPROVE_OPTION) {
                // Selecciona el archivo seleccionado
                File archivo = escojer.getSelectedFile();
                // Obtenemos direccion del archivo
                ruta = archivo.getPath(); 
                
                Archivo clase = new Archivo(ruta);
                
                ArrayList<String[]> datos = new ArrayList<>();
                
                if (archivo.getName().endsWith("csv")) {
                    datos = clase.leer();
                    
                    model = new DefaultTableModel();
                    model.setColumnIdentifiers(datos.get(0));
                    
                    for (int i = 1; i < datos.size(); i++) {
                        model.addRow(datos.get(i));
                    }
                    
                    jTable1.setModel(model);
                }
                
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_importarbtnActionPerformed

