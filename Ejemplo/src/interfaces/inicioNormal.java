    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        model = (DefaultTableModel) jTable1.getModel();
        int numcol = model.getColumnCount();
        int numrow = model.getRowCount();
        
        String datos = "";
        
        for (int i = 0; i < numcol; i++) {
            datos += model.getColumnName(i);
            if(i != (numcol - 1)){
                datos += "|";
            }
        }
        
        datos += "\n";
        
        for (int fila = 0; fila < numrow; fila++) {
            for (int columna = 0; columna < numcol; columna++) {
                Object valor = model.getValueAt(fila, columna);
                if (valor != null) {
                    datos += valor;
                }
                if(columna != (numcol - 1)){
                    datos += "|";
                }
            }
            datos += "\n";
        }
        
        Archivo clase = new Archivo(ruta);
        clase.escribir(datos);
        
    }//GEN-LAST:event_jButton3ActionPerformed

    
